CREATE PROCEDURE spInsertPerson (@Name TEXT,@Surname TEXT,@Age INTEGER,@JobId INTEGER) AS
    INSERT INTO Persons (Name,Surname,Age,JobId)
    VALUES (@Name,@Surname,@Age,@JobId);
    SELECT * FROM Persons WHERE ROWID=LAST_INSERT_ROWID();
GO
